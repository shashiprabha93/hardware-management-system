    private void btnEmpAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEmpAddActionPerformed
        // TODO add your handling code here:
        Employee employee = new Employee();
        try {

            EmployeeController employeeController = new EmployeeController();
            employee.setEmployeeName(txtEmpName.getText().trim());
            employee.setEmployeeAddr(txtEmpAdd.getText().trim());
            employee.setEmployeeGender(buttonGroup1.getSelection().getActionCommand());
            employee.setEmployeeNic(txtEmpNIC.getText().trim());
            employee.setEmployeeCon(txtEmpCon.getText().trim());
            employee.setCreatedDate(new Date());
            employee.setStatus(true);
            employee.setDOB(jDateChooser1.getDateFormatString());
            employee.setBasicSal(Float.valueOf(txtBasicSal.getText()));
            employee.setBonus(Float.valueOf(txtBonus.getText()));
            employee.setCurrentStatus(cmbCurrentStatus.getSelectedItem().toString());
            employee.setMartialStatus(buttonGroup2.getSelection().getActionCommand());

            if (employeeController.save(employee)) {
                JOptionPane.showMessageDialog(this, "Employee added successfully. ", "Success Message", JOptionPane.INFORMATION_MESSAGE);

                txtEmpName.requestFocus();
            } else {
                JOptionPane.showMessageDialog(this, "Error in Employee adding.", "Error Message", JOptionPane.ERROR_MESSAGE);
                txtEmpName.requestFocus();
            }
    }//GEN-LAST:event_btnEmpAddActionPerformed
    catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Error Message" + ex);
        }
        EmployeeController employeeController1 = new EmployeeController();
        AttendanceId attendanceId = new AttendanceId();
        attendanceId.setAttDate("Main");
        attendanceId.setEmpId(employee.getEmployeeId());
        Attendance attendance = new Attendance();

        attendance.setAttId(attendanceId);
        attendance.setEmployeeName(txtEmpName.getText().trim());
        attendance.setMain(true);
        attendance.setStatus(true);
        employeeController1.save(attendance);

    }

