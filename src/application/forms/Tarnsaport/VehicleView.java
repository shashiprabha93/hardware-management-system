    private void txtVehicleSearchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtVehicleSearchKeyReleased
        final TableRowSorter<TableModel> sorter = new TableRowSorter<TableModel>(modelVehicle);
        tblVehicle.setRowSorter(sorter);
        sorter.setRowFilter(RowFilter.regexFilter("(?i)" + txtVehicleSearch.getText()));
        checkBtnVehicle();
        //        in here "?!" act as "like" operator in sql
    }//GEN-LAST:event_txtVehicleSearchKeyReleased
