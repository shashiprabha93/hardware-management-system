    private void btnUpdateVehicleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateVehicleActionPerformed

        int selectedRow = tblVehicle.convertRowIndexToModel(tblVehicle.getSelectedRow());
        //        when we are going to normaly update the table row below way is ok, we can take the table model of supplier
        //fill up the table model with row value and column value then passing to supplier update form "setsupplierupdate"
        // in update form will get the value form supplier view and set value to update form text boxes.

        //usage of "converRwIndexModel is if we upadte after serching we need to take the model of table

        VehicleUpdate vehicleUpdate = new VehicleUpdate(this,true);
        Transport transport = new Transport();
        transport.setVehicleId(Integer.valueOf(modelVehicle.getValueAt(selectedRow, 0).toString()));
        transport.setvNumber(modelVehicle.getValueAt(selectedRow, 1).toString());
        transport.setModel(modelVehicle.getValueAt(selectedRow, 2).toString());
        transport.setType(modelVehicle.getValueAt(selectedRow, 3).toString());

        vehicleUpdate.setVehicleUpdate(transport);
        vehicleUpdate.setVisible(true);
        transportController.getAllVehicle(modelVehicle);
        checkBtnVehicle();
        txtVehicleSearch.requestFocus();
    }//GEN-LAST:event_btnUpdateVehicleActionPerformed

    private void btnDeleteVehicleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteVehicleActionPerformed

        int status = JOptionPane.showConfirmDialog(this, "Do you want to delete this Vehicle ?", "Delete Vehicle", JOptionPane.YES_NO_OPTION);
        if (status == JOptionPane.YES_OPTION) {
            int selectedRow = tblVehicle.convertRowIndexToModel(tblVehicle.getSelectedRow());
            int vehicleId = Integer.valueOf(modelVehicle.getValueAt(selectedRow, 0).toString());
            Transport transport = transportController.findVehicleId(vehicleId);
            transport.setStatus(false);

            if (transportController.update(transport)) {
                modelVehicle.removeRow(selectedRow);
                checkBtnVehicle();
                JOptionPane.showMessageDialog(this, "Vehicle deleted successfully.", "Deletion Message", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(this, "Error in Vehicle deleting.", "Error Message", JOptionPane.ERROR_MESSAGE);
            }
        }
        txtVehicleSearch.requestFocus();
    }//GEN-LAST:event_btnDeleteVehicleActionPerformed

