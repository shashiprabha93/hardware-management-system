    private void btnCustomerUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCustomerUpdateActionPerformed
        Customer customer = customerController.findCustomerById(customerId);
        customer.setCustName(txtCustName.getText().trim());
        customer.setCustPhone(txtCustPhone.getText());
        customer.setCustAddress(txtAddress.getText().trim());
        
         if (customerController.update(customer)) {
                       JOptionPane.showMessageDialog(this, "Customer updated successfully .", "Update Message", JOptionPane.INFORMATION_MESSAGE);
                        this.dispose();
                    } else {
                        JOptionPane.showMessageDialog(this, "Error in Customer updating", "Error Message", JOptionPane.ERROR_MESSAGE);
                        txtCustName.requestFocus();
                    }
   

    }//GEN-LAST:event_btnCustomerUpdateActionPerformed

