    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // Add button action
        OtherExpenses otherExpenses = new OtherExpenses();
        OtherExpensesController otherExpensesController = new OtherExpensesController();
        
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        Date date = new Date();
        date=dtmDate.getDate();
        String expDate = (String) sdf.format(dtmDate.getDate());
        otherExpenses.setExpenseDate(expDate);
        
        otherExpenses.setExpenseDate(expDate);
        otherExpenses.setExpenseElectricity(txtElectricityBill.getText().trim());
        otherExpenses.setExpenseWater(txtWaterBill.getText().trim());
        otherExpenses.setExpenseInsurance(txtInsurance.getText().trim());
        otherExpenses.setExpenseRental(txtRentals.getText().trim());
        otherExpenses.setExpenseOther(txtOthers.getText().trim());
        otherExpenses.setExpenseTotal(lblTotal.getText().trim());
        otherExpenses.setCreatedAt(new Date());
        otherExpenses.setUpdatedAt(new Date());
        otherExpenses.setStatus(true);

         if (otherExpensesController.save(otherExpenses)) {
            JOptionPane.showMessageDialog(this, "Other Expenses added successfully. ", "Success Message", JOptionPane.INFORMATION_MESSAGE);

            dtmDate.requestFocus();
            clearFileds();
        } else {
            JOptionPane.showMessageDialog(this, "Error in adding Other Expenses.", "Error Message", JOptionPane.ERROR_MESSAGE);
           
            dtmDate.requestFocus();
           clearFileds();
    }  
        
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnCalculateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalculateActionPerformed
         // calculate button code
       
        double electricity=Double.parseDouble(txtElectricityBill.getText());
        double water=Double.parseDouble(txtWaterBill.getText());
        double insurance=Double.parseDouble(txtInsurance.getText());
        double rental=Double.parseDouble(txtRentals.getText());
        double other=Double.parseDouble(txtOthers.getText());
        
        double total=electricity+water+insurance+rental+other;
        lblTotal.setText(String.valueOf(total));
   
    }//GEN-LAST:event_btnCalculateActionPerformed

