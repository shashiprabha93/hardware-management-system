    private void btnUpdateOtherExpensesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateOtherExpensesActionPerformed
        //Update button action 
        
        int selectedRow = tblOtherExpense.convertRowIndexToModel(tblOtherExpense.getSelectedRow());
        OtherExpenseUpdate otherExpenseUpdate = new OtherExpenseUpdate(this, true);
        OtherExpenses otherExpenses = new OtherExpenses();
        otherExpenses.setExpenseId(Integer.valueOf(modelOtherExpenses.getValueAt(selectedRow, 0).toString()));
        otherExpenses.setExpenseDate(modelOtherExpenses.getValueAt(selectedRow, 1).toString());
        otherExpenses.setExpenseElectricity(modelOtherExpenses.getValueAt(selectedRow, 2).toString());
        otherExpenses.setExpenseWater(modelOtherExpenses.getValueAt(selectedRow, 3).toString());
        otherExpenses.setExpenseInsurance(modelOtherExpenses.getValueAt(selectedRow, 4).toString());
        otherExpenses.setExpenseRental(modelOtherExpenses.getValueAt(selectedRow, 5).toString());
        otherExpenses.setExpenseOther(modelOtherExpenses.getValueAt(selectedRow, 6).toString());
        otherExpenses.setExpenseTotal(modelOtherExpenses.getValueAt(selectedRow, 7).toString());
        otherExpenseUpdate.setOtherExpensesUpdate(otherExpenses);
        otherExpenseUpdate.setVisible(true);
        otherExpensesController.getAllOtherExpenses(modelOtherExpenses);
        checkBtnOtherExpenses();
        txtOtherExpensesSearch.requestFocus();

    }//GEN-LAST:event_btnUpdateOtherExpensesActionPerformed

