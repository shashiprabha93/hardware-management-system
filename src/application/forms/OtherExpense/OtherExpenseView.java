    private void txtOtherExpensesSearchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtOtherExpensesSearchKeyReleased
       final TableRowSorter<TableModel> sorter = new TableRowSorter<TableModel>(modelOtherExpenses);
        tblOtherExpense.setRowSorter(sorter);
        sorter.setRowFilter(RowFilter.regexFilter("(?i)" + txtOtherExpensesSearch.getText()));
        checkBtnOtherExpenses();
        //        in here "?!" act as "like" operator in sql

    }//GEN-LAST:event_txtOtherExpensesSearchKeyReleased

    private void btnDeleteOtherExpensesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteOtherExpensesActionPerformed
       // Delete button action
        int status = JOptionPane.showConfirmDialog(this, "Do you want to delete the other expenses ?", "Delete Other Expenses", JOptionPane.YES_NO_OPTION);
        if (status == JOptionPane.YES_OPTION) {
        int selectedRow = tblOtherExpense.convertRowIndexToModel(tblOtherExpense.getSelectedRow());
            int expenseId = Integer.valueOf(modelOtherExpenses.getValueAt(selectedRow, 0).toString());
            OtherExpenses otherExpenses = otherExpensesController.findOtherExpensesById(expenseId);
            otherExpenses.setStatus(false);
            otherExpenses.setDeletedAt(new Date());
            if (otherExpensesController.update(otherExpenses)) {
                modelOtherExpenses.removeRow(selectedRow);
                checkBtnOtherExpenses();
                JOptionPane.showMessageDialog(this, "Other Expenses deleted successfully.", "Deletion Message", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(this, "Error in deleting Other Expenses.", "Error Message", JOptionPane.ERROR_MESSAGE);
            }
        }
        txtOtherExpensesSearch.requestFocus();
   
    }//GEN-LAST:event_btnDeleteOtherExpensesActionPerformed

