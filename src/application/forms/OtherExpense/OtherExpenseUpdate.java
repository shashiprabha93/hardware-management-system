    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
         //Update Button code
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        Date date = new Date();
        date=dtmDate.getDate();
        String expDate = (String) sdf.format(dtmDate.getDate());
        
        OtherExpenses otherExpenses=otherExpensesController.findOtherExpensesById(expenseId);
        
        otherExpenses.setExpenseDate(dtmDate.getDate().toString());
        otherExpenses.setExpenseElectricity(txtElectricityBill.getText().trim());
        otherExpenses.setExpenseWater(txtWaterBill.getText().trim());
        otherExpenses.setExpenseInsurance(txtInsurance.getText().trim());
        otherExpenses.setExpenseOther(txtOthers.getText().trim());
        otherExpenses.setExpenseTotal(txtOthers.getText().trim());
        otherExpenses.setExpenseTotal(lblTotal.getText().trim());
        otherExpenses.setUpdatedAt(new Date()); 

       
        
         if (otherExpensesController.update(otherExpenses)) {
            JOptionPane.showMessageDialog(this, "Other Expenses updated successfully. ", "Success Message", JOptionPane.INFORMATION_MESSAGE);
             dtmDate.requestFocus();
            //clearFileds();
        } else {
            JOptionPane.showMessageDialog(this, "Error in updating Other Expenses.", "Error Message", JOptionPane.ERROR_MESSAGE);
           dtmDate.requestFocus();
           //clearFileds();
    }  
    }//GEN-LAST:event_jButton2ActionPerformed

