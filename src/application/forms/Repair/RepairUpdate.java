    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // Repair button code
        
        SimpleDateFormat date = new SimpleDateFormat("yyyy-MM-dd");
        String subDate = date.format(datePickerSubmit.getCalendar().getTime());
        String dueDate = date.format(datePickerDueDate.getCalendar().getTime());
        Date date1 = new Date();
        date1=datePickerSubmit.getDate();
        String sDate = (String) date.format(datePickerSubmit.getDate());
        date1=datePickerDueDate.getDate();
        String dDate1 = (String) date.format(datePickerDueDate.getDate());
        Repair repair = repairController.findRepairById(repairId);
        repair.setRepairName(txtRepairName.getText().trim());
        repair.setRepairPhone(txtPhone.getText());
        repair.setRepairDuedate(dDate1);
        repair.setRepairSubmitdate(sDate);
        repair.setRepairDuedate(datePickerDueDate.getDate().toString());
        repair.setRepairNotes(txtNotes.getText().trim());
        repair.setUpdatedAt(new Date());

        
        if (repairController.checkRepairMobile(repair) == true) {
            JOptionPane.showMessageDialog(rootPane, "Repair phone no already exists ", "Warning Message", JOptionPane.WARNING_MESSAGE);
            txtPhone.requestFocus();
        } else {

            int status = repairController.isRepairExists(repair);
            switch (status) {
                case 0:
                    JOptionPane.showMessageDialog(this, "Repair Details already exists !", "Warning Message", JOptionPane.WARNING_MESSAGE);
                    txtPhone.requestFocus();
                    break;
                case 1:
                    if (repairController.update(repair)) {
                        JOptionPane.showMessageDialog(this, "Repair Details updated successfully .", "Update Message", JOptionPane.INFORMATION_MESSAGE);
                        this.dispose();
                    } else {
                        JOptionPane.showMessageDialog(this, "Error in updating Repair Details", "Error Message", JOptionPane.ERROR_MESSAGE);
                        txtRepairName.requestFocus();
                    }
                    break;
                case -1:
                    JOptionPane.showMessageDialog(this, "Error in updating Repair Details", "Error Message", JOptionPane.ERROR_MESSAGE);
                    txtRepairName.requestFocus();
                    break;
                default:
                    JOptionPane.showMessageDialog(this, "Error in updating Repair Details", "Error Message", JOptionPane.ERROR_MESSAGE);
                    txtRepairName.requestFocus();
                    break;
            }
        }

    }//GEN-LAST:event_btnUpdateActionPerformed

