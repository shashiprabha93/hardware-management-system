    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
              // Add button code
        Repair repair = new Repair();
        RepairController repairController = new RepairController();
        
        repair.setRepairName(txtRepairName.getText().trim());
        repair.setRepairPhone(txtRepairPhone.getText());
        
        SimpleDateFormat date = new SimpleDateFormat("yyyy-MM-dd");
     
        String subDate = date.format(datePickerSubmit.getCalendar().getTime());
        String dueDate = date.format(datePickerDueDate.getCalendar().getTime());
        
        
        Date date1 = new Date();
        date1=datePickerSubmit.getDate();
        String sDate = (String) date.format(datePickerSubmit.getDate());
        
        repair.setRepairSubmitdate(sDate);
        
        date1=datePickerDueDate.getDate();
        String dDate1 = (String) date.format(datePickerDueDate.getDate());
        
        repair.setRepairDuedate(dDate1);
        repair.setRepairNotes(txtNotes.getText().trim());
        repair.setCreatedAt(new Date());
        repair.setUpdatedAt(new Date());
        repair.setStatus(true);

        if (repairController.checkRepairMobile(repair) == true) {
            JOptionPane.showMessageDialog(rootPane, "Repairer phone no already exists ", "Warning Message", JOptionPane.WARNING_MESSAGE);
            txtRepairPhone.requestFocus();
        } else {

            int status = repairController.isRepairExists(repair);
            switch (status) {
                case 0:
                    JOptionPane.showMessageDialog(this, "Repairer already exists !", "Warning Message", JOptionPane.WARNING_MESSAGE);
                    txtRepairName.requestFocus();
                    break;
                case 1:
                    if (repairController.save(repair)) {
                        JOptionPane.showMessageDialog(this, "Repair Details added successfully. ", "Success Message", JOptionPane.INFORMATION_MESSAGE);
                        repairController.getAllRepair(stockView.modelRepair);
                        clearFileds();
                        txtRepairName.requestFocus();
                    } else {
                        JOptionPane.showMessageDialog(this, "Error in adding Repair Details.", "Error Message", JOptionPane.ERROR_MESSAGE);
                        txtRepairName.requestFocus();
                    }
                    break;
                case -1:
                    JOptionPane.showMessageDialog(this, "Error in adding Repair Details.", "Error Message", JOptionPane.ERROR_MESSAGE);
                    txtRepairName.requestFocus();

                    break;
                default:
                    JOptionPane.showMessageDialog(this, "Error in adding Repair Details.", "Error Message", JOptionPane.ERROR_MESSAGE);
                    txtRepairName.requestFocus();
                    break;
            }

    }//GEN-LAST:event_btnAddActionPerformed
    }
